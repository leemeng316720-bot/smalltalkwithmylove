// Edge Function 入口
// =========================================
serve(async (req) => {
  // CORS 预检
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: CORS_HEADERS })
  }

  try {
    const { userQuestion } = await req.json()
    
    if (!userQuestion) {
      return new Response(
        JSON.stringify({ error: '缺少 userQuestion' }),
        { status: 400, headers: CORS_HEADERS }
      )
    }

    // 构建完整的系统prompt
    const fullPrompt = injectKnowledgeToPrompt(SYSTEM_PROMPT, userQuestion)
    
    const messages = [
      { role: 'system', content: fullPrompt },
      { role: 'user', content: userQuestion }
    ]

    const content = await callMoonshotAPI(messages, 0.85, 4000)
    const parsed = safeParseJSON(content)

    if (!parsed.roundtable || !Array.isArray(parsed.roundtable)) {
      return new Response(
        JSON.stringify({ error: 'API 返回格式不正确', raw: content }),
        { status: 500, headers: CORS_HEADERS }
      )
    }

    return new Response(
      JSON.stringify({ roundtable: parsed.roundtable }),
      { headers: CORS_HEADERS }
    )

  } catch (err) {
    console.error('Roundtable error:', err)
    return new Response(
      JSON.stringify({ error: err.message }),
      { status: 500, headers: CORS_HEADERS }
    )
  }
})
