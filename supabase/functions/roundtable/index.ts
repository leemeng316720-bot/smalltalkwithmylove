// 将知识库注入 prompt
function injectKnowledgeToPrompt(basePrompt: string, userQuestion: string): string {
  let prompt = basePrompt.replace('{userQuestion}', userQuestion)
  
  // 为每个角色注入知识库
  const knowledgeText = Object.entries(KNOWLEDGE_BASE).map(([name, chunks]) => {
    return `## ${name} 的知识库摘录\n${chunks.map(c => `[${c.type}] ${c.source}: ${c.text}`).join('\n')}`
  }).join('\n\n')
  
  prompt += '\n\n【知识库】以下是各位人物公开作品的摘录和访谈记录，回答时请基于这些内容，并引用具体出处：\n\n' + knowledgeText
  
  return prompt
}


