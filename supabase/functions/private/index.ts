serve(async (req) => {
  // CORS 预检
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: CORS_HEADERS })
  }

  try {
    const { partnerName, userQuestion, history } = await req.json()
    
    if (!partnerName || !userQuestion) {
      return new Response(
        JSON.stringify({ error: '缺少 partnerName 或 userQuestion' }),
        { status: 400, headers: CORS_HEADERS }
      )
    }

    const basePrompt = PRIVATE_PROMPTS[partnerName]
    if (!basePrompt) {
      return new Response(
        JSON.stringify({ error: `未知角色: ${partnerName}` }),
        { status: 400, headers: CORS_HEADERS }
      )
    }

    // 构建完整的系统prompt
    let systemPrompt = basePrompt
    if (history && history.length > 0) {
      systemPrompt += '\n\n【对话历史】\n你记得此前的对话：\n' + history.join('\n') +
        '\n\n请基于你的身份和风格，回应用户的最新问题。'
    }
    
    const apiMessages = [
      { role: 'system', content: systemPrompt },
      { role: 'user', content: userQuestion }
    ]

    const content = await callMoonshotAPI(apiMessages, 0.8, 2000)

    return new Response(
      JSON.stringify({ content }),
      { headers: CORS_HEADERS }
    )

  } catch (err) {
    console.error('Private chat error:', err)
    return new Response(
      JSON.stringify({ error: err.message }),
      { status: 500, headers: CORS_HEADERS }
    )
  }
})
