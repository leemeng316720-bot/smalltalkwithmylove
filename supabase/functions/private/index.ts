// 调用 Moonshot API（带重试）
// =========================================
async function callMoonshotAPI(messages: any[], temperature: number, maxTokens: number, retryCount = 0) {
  const retryDelay = RETRY_DELAY
  
  try {
    const controller = new AbortController()
    const timeoutId = setTimeout(() => controller.abort(), TIMEOUT)
    
    const response = await fetch(API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${MOONSHOT_API_KEY}`
      },
      body: JSON.stringify({
        model: 'moonshot-v1-128k',
        messages: messages,
        temperature: temperature,
        max_tokens: maxTokens
      }),
      signal: controller.signal
    })
    
    clearTimeout(timeoutId)

    if (response.status === 429 && retryCount < MAX_RETRIES) {
      console.log(`[API 429] 引擎过载，${retryDelay/1000}秒后重试 (${retryCount + 1}/${MAX_RETRIES})...`)
      await new Promise(resolve => setTimeout(resolve, retryDelay))
      return callMoonshotAPI(messages, temperature, maxTokens, retryCount + 1)
    }

    if (!response.ok) {
      const errorText = await response.text()
      let errorDetail = errorText
      try {
        const errorJson = JSON.parse(errorText)
        errorDetail = errorJson.error?.message || errorJson.message || errorText
      } catch (e) {}
      throw new Error(`API ${response.status}: ${errorDetail}`)
    }

    const data = await response.json()
    return data.choices[0].message.content
  } catch (err) {
    if (retryCount < MAX_RETRIES && (err.message?.includes('fetch') || err.message?.includes('network') || err.message?.includes('Failed to fetch'))) {
      console.log(`[网络错误] ${retryDelay/1000}秒后重试 (${retryCount + 1}/${MAX_RETRIES})...`)
      await new Promise(resolve => setTimeout(resolve, retryDelay))
      return callMoonshotAPI(messages, temperature, maxTokens, retryCount + 1)
    }
    throw err
  }
}

