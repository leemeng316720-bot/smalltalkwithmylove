// 将知识库注入单人对话 prompt
function injectKnowledge(partnerName: string, basePrompt: string): string {
  const chunks = KNOWLEDGE_BASE[partnerName]
  if (!chunks || chunks.length === 0) return basePrompt
  
  const knowledgeText = chunks.map(c => `[${c.type}] ${c.source}: ${c.text}`).join('\n')
  
  return basePrompt + '\n\n【知识库】以下是你公开作品的摘录和访谈记录，请基于这些内容回答，并引用具体出处：\n\n' + knowledgeText
}

